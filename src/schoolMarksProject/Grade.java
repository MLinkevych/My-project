package schoolMarksProject;

public class Grade {
    private int math;
    private int eng;
    private int ukr;


    public Grade() {

    }

    public int getMath() {
        return math;
    }

    public void setMath(int math) {
        this.math = math;
    }

    public int getEng() {
        return eng;
    }

    public void setEng(int eng) {
        this.eng = eng;
    }

    public int getUkr() {
        return ukr;
    }

    public void setUkr(int ukr) {
        this.ukr = ukr;
    }



}
